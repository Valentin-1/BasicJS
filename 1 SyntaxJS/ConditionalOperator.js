(n == 3) ? 'trei':
(n == 4) ? 'patru':
(n == 5) ? 'cinci':
(n == 6) ? 'sase':
(n == 7) ? 'sapte':
(n == 8) ? 'opt':
(n == 9) ? 'noua':
'Numarul introdus nu corespunde cerintei';

alert (message);

